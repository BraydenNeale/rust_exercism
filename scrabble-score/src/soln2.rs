pub fn score(word : &'static str) -> u32 {
    word.to_lowercase().chars().fold(0, |score, x| score + get_letter_value(x))
}

fn get_letter_value(letter : char) -> u32 {
    match letter {
        'a' | 'e' | 'i' | 'o' | 'u' | 'l' | 'n' | 'r' | 's' | 't' => 1,
        'd' | 'g' => 2,
        'b' | 'c' | 'm' | 'p' => 3,
        'f' | 'h' | 'v' | 'w' | 'y' => 4,
        'k' => 5,
        'j' | 'x' => 8,
        'q' | 'z' => 10,
        _ => 0,
    }
}